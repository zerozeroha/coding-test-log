/**
 * [문제 설명]
 * 주어진 문자열 배열(str_list)에서 "l" 또는 "r" 중 먼저 등장하는 문자를 기준으로
 * 해당 방향의 요소들만 잘라내서 새 배열로 반환하는 함수.
 *
 * - "l"이 먼저 등장하면 → 해당 인덱스 이전 요소들을 반환
 * - "r"이 먼저 등장하면 → 해당 인덱스 이후 요소들을 반환
 * - 둘 다 없거나, 기준 인덱스 쪽에 요소가 없으면 빈 배열 반환
 *
 * 예: ["u", "u", "l", "r"] → ["u", "u"]
 */

function solution(str_list) {
  // 배열을 앞에서부터 순차적으로 탐색
  for (let i = 0; i < str_list.length; i++) {

    // "l"이 처음 등장한 경우 → 해당 인덱스 이전 요소만 잘라서 반환
    if (str_list[i] === "l") {
      /**
       * slice(0, i)는 0번 인덱스부터 i-1까지를 잘라 반환함
       * ⚠️ slice()는 원본 배열을 수정하지 않고 결과를 '반환'만 하므로,
       * 반드시 return 키워드로 반환하거나 변수에 저장해야 의미가 있음!
       */
      return str_list.slice(0, i);
    }

    // "r"이 처음 등장한 경우 → 해당 인덱스 다음 요소부터 끝까지 반환
    else if (str_list[i] === "r") {
      /**
       * slice(i + 1)은 i 다음 인덱스부터 끝까지 자른 배열을 반환
       * ⚠️ 이 또한 return 없이 호출만 하면 아무 효과 없음!
       */
      return str_list.slice(i + 1);
    }
  }

  // 반복을 다 돌았는데도 "l"이나 "r"이 없을 경우 → 빈 배열 반환
  return [];
}
