/**
 * 문제 설명
 * 정수 리스트 num_list가 주어질 때,
 * 첫 번째로 나오는 음수의 인덱스를 return하는 solution 함수를 완성하세요.
 * 음수가 없다면 -1을 return합니다.
 *
 * 제한사항
 * - 5 ≤ num_list.length ≤ 100
 * - -10 ≤ num_list[i] ≤ 100
 *
 * 입출력 예
 * - [12, 4, 15, 46, 38, -2, 15] → 5 (처음 등장한 음수 -2의 인덱스)
 * - [13, 22, 53, 24, 15, 6] → -1 (음수 없음)
 *
 * 헷갈렸던 부분 정리
 * - ❌ `num_list.find(i)`는 "값"을 찾는 함수이지 "인덱스"를 반환하지 않음
 * - 원하는 것은 인덱스이므로, 반복문에서 `i` 자체를 return해야 함
 * - `for`문으로 인덱스를 직접 순회하면서 조건문으로 음수인지 확인하는 게 가장 직관적임
 */

function solution(num_list) {
  for (let i = 0; i < num_list.length; i++) {
    if (num_list[i] < 0) {
      return i; // 음수 발견 → 인덱스 반환
    }
  }
  return -1; // 끝까지 음수 없음
}
