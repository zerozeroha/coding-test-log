/**
 * 문제 설명
 * 정수 배열 arr가 주어집니다. 이때 arr의 원소는 1 또는 0입니다.
 * 정수 idx가 주어졌을 때, idx 이상이면서 값이 1인 가장 작은 인덱스를 찾아서 반환합니다.
 * 만약 그러한 인덱스가 없다면 -1을 반환합니다.
 *
 * 제한사항
 * - 3 ≤ arr.length ≤ 100000
 * - arr의 원소는 전부 0 또는 1입니다.
 *
 * 입출력 예시
 * solution([0, 0, 0, 1], 1) → 3
 * solution([1, 0, 0, 1, 0, 0], 4) → -1
 * solution([1, 1, 1, 1, 0], 3) → 3
 *
 * 헷갈릴 수 있는 포인트
 * - 조건은 "idx보다 **크거나 같은**" 인덱스를 의미합니다 (>=)
 *   → idx보다 **큰**(>) 게 아님에 주의!
 * - arr[i]가 1인 조건을 같이 체크해야 합니다.
 */

function solution(arr, idx) {
  for (let i = 0; i < arr.length; i++) {
    if (i >= idx && arr[i] === 1) {
      return i;
    }
  }
  return -1;
}
