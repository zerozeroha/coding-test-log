/**
 * 문제 설명
 * 정수 n의 각 자릿수를 큰 것부터 작은 순서로 정렬해
 * 하나의 정수로 다시 합친 결과를 반환하는 함수.
 *
 * 제한 사항
 * - n은 1 이상 8,000,000,000 이하인 자연수
 *
 * 예시
 * 입력: 118372 → 출력: 873211
 */

function solution(n) {
  // 1. 숫자를 문자열로 변환 후, 배열로 나눔 → ['1','1','8','3','7','2']
  // 2. 내림차순 정렬 (숫자 정렬이므로 compare 함수 필요)
  // 3. 다시 하나의 문자열로 합치고, Number로 변환
  return Number(
    String(n) // 숫자를 문자열로 변환
    .split('') // 한 글자씩 배열로 만듦
    .sort((a, b) => b - a) // 숫자 기준 내림차순 정렬
    .join('') // 다시 문자열로 합치기
  );
}

